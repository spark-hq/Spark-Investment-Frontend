// SUMMARY STATISTICS
export const investmentSummary = {
  totalInvested: investments.reduce((sum, inv) => sum + inv.investedAmount, 0),
  totalCurrentValue: investments.reduce((sum, inv) => sum + inv.currentValue, 0),
  totalReturns: investments.reduce((sum, inv) => sum + inv.returns, 0),
  totalInvestments: investments.length,
  profitableInvestments: investments.filter((inv) => inv.status === 'profit').length,
  lossInvestments: investments.filter((inv) => inv.status === 'loss').length,
};

investmentSummary.overallReturnsPercentage = (
  (investmentSummary.totalReturns / investmentSummary.totalInvested) *
  100
).toFixed(2);

// PLATFORM-WISE SUMMARY
export const platformSummary = investmentFilters.platforms
  .filter((p) => p !== 'All')
  .map((platform) => {
    const platformInvestments = investments.filter((inv) => inv.platform === platform);
    const invested = platformInvestments.reduce((sum, inv) => sum + inv.investedAmount, 0);
    const current = platformInvestments.reduce((sum, inv) => sum + inv.currentValue, 0);
    const returns = current - invested;

    return {
      platform,
      count: platformInvestments.length,
      invested,
      currentValue: current,
      returns,
      returnsPercentage: invested > 0 ? ((returns / invested) * 100).toFixed(2) : 0,
    };
  });

